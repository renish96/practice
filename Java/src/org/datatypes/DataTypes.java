package org.datatypes;

public class DataTypes {
	public static void main(String[] args) {
		byte age = 26;
		short id = 23254;
		int pin = 373923776;
		long accNo = 4648957287582l;
		float mark = 95.6576f;
		double check = 45.443564776d;
		char grade = 'A';
		boolean s = false;
		String email = "john234@g.com";
		System.out.println(age + "\n" + id + "\n" + pin + "\n" + accNo + "\n" + mark + "\n" + check + "\n" + grade
				+ "\n" + s + "\n" + email);
	}
}
